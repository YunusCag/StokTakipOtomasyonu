    private void tableCalisanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCalisanMouseClicked
        // TODO add your handling code here:
       int selectedRow = tableCalisan.getSelectedRow();
        if (allCalisan.size() > selectedRow && selectedRow >= 0) {
            Calisan temp = allCalisan.get(selectedRow);
            tfID.setText(temp.getCalisanNo() + "");
            tfCalisanAdi.setText(temp.getfName());
            tfSoyadi.setText(temp.getlName());
            cbPosition.setSelectedItem(temp.getPosition());
            spinMaas.setValue(temp.getSalary());
            dPickerBaslama.setDate(temp.getDob());
            if(temp.getCinsiyet()=='E'){
                rbtnErkek.setSelected(true);
                rbtnKadin.setSelected(false);
      
            }else{
                rbtnKadin.setSelected(true);
                rbtnErkek.setSelected(false);
            }
            Point point = evt.getPoint();
            int column = tableCalisan.columnAtPoint(point);
            if (column == 6) {
                Runnable task = () -> {
                    dataSource.openConnection();
                    dataSource.deleteCalisan(temp.getCalisanNo());
                    dataSource.closeConnection();
                    tabloDoldur("");

                };
                Thread thread = new Thread(task);
                thread.start();
        }

        //cbPosition.setSelectedItem(temp.getPosition());
        }
    }//GEN-LAST:event_tableCalisanMouseClicked


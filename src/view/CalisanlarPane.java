    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String fName = tfCalisanAdi.getText().trim();
        String lName = tfSoyadi.getText().trim();
        int index = cbPosition.getSelectedIndex();
        char gender='F';
        if(rbtnErkek.isSelected()){
            gender='E';
        }else if(rbtnKadin.isSelected()){
            gender='K';
        }
        Date baslamaTarihi=dPickerBaslama.getDate();
        int maasi=(int)spinMaas.getValue();
        if (!fName.equals("") && !lName.equals("")&& index !=-1 &&gender!='F') {
            String position=cbPosition.getSelectedItem().toString();
            Calisan calisan = new Calisan(fName,lName,baslamaTarihi,gender,position,maasi);
            Runnable task = () -> {
                dataSource.openConnection();
                boolean control = dataSource.addCalisan(calisan);
                System.out.println("Calisan Eklendi");
                dataSource.closeConnection();
                System.out.println("Thread name:" + Thread.currentThread().getName());
                if (control) {
                    lblError.setVisible(true);
                    lblError.setForeground(Color.green);
                    lblError.setText("Calisan kayıt edildi");
                    tabloDoldur("");
                } else {
                    lblError.setVisible(true);
                    lblError.setForeground(Color.red);
                    lblError.setText("Calisan kayıt edilirken hata oluştu");
                }
            };
            Thread thread = new Thread(task);
            thread.start();

        } else {
            lblError.setVisible(true);
            lblError.setForeground(Color.red);
            lblError.setText("Gerekli alanları doldurunuz.");
        }
        
    }//GEN-LAST:event_btnAddActionPerformed


    private void tabloDoldur(String search) {
        dataSource = new DataSource();
        DefaultTableModel model = (DefaultTableModel) tableUser.getModel();
        model.setNumRows(0);

        Runnable task = () -> {
            dataSource.openConnection();
            allUser = dataSource.getAllEvrakUsers(search);
            dataSource.closeConnection();
            if (allUser.size() > 0) {
                allUser.forEach((e) -> {
                    String userName = "";
                    if (e.getUserName().length() > 50) {
                        userName = e.getUserName().substring(0, 49);
                    } else {
                        userName = e.getUserName();
                    }

                    model.addRow(new Object[]{e.getUserNo(), userName, e.getPosition() + "", e.getPassword()});
                });
            }
        };
        Thread thread = new Thread(task);
        thread.start();
    }

    private void tableUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableUserMouseClicked
        // TODO add your handling code here:
        int selectedRow = tableUser.getSelectedRow();
        if (allUser.size() > selectedRow && selectedRow >= 0) {
            User temp = allUser.get(selectedRow);
            tfID.setText(temp.getUserNo() + "");
            tfKullaniciAdi.setText(temp.getUserName());
            tfPassword.setText(temp.getPassword());
            if (temp.getPosition() == 'A') {
                cbPosition.setSelectedIndex(0);
            } else if (temp.getPosition() == 'U') {
                cbPosition.setSelectedIndex(1);
            }
            Point point = evt.getPoint();
            int column = tableUser.columnAtPoint(point);
            if (column == 4) {
                Runnable task = () -> {
                    dataSource.openConnection();
                    dataSource.deleteUser(temp.getUserNo());
                    dataSource.closeConnection();
                    tabloDoldur("");

                };
                Thread thread = new Thread(task);
                thread.start();
        }

        //cbPosition.setSelectedItem(temp.getPosition());
        }
    }//GEN-LAST:event_tableUserMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int id = 0;
        if (!tfID.getText().equals("")) {
            id = Integer.parseInt(tfID.getText());
        }
        String name = tfKullaniciAdi.getText();
        String password = tfPassword.getText();
        int index = cbPosition.getSelectedIndex();
        char position = 'C';
        if (index == 0) {
            position = 'A';
        } else if (index == 1) {
            position = 'U';
        }

        if (!name.equals("") && !password.equals("") && (position != 'C')) {
            User user = new User(id, name, password, position, 0);
            Runnable task = () -> {
                dataSource.openConnection();
                boolean control = dataSource.updateUser(user);
                dataSource.closeConnection();
                System.out.println("Thread name:" + Thread.currentThread().getName());

                if (control) {
                    lblError.setVisible(true);
                    lblError.setForeground(Color.green);
                    lblError.setText("Kullanıcı kayıdı güncellendi");
                    tabloDoldur("");
                } else {
                    lblError.setVisible(true);
                    lblError.setForeground(Color.red);
                    lblError.setText("Kullanıcı kayıdı güncellenirken hata oluştu");
                }
            };
            Thread thread = new Thread(task);
            thread.start();

        } else {
            lblError.setVisible(true);
            lblError.setForeground(Color.red);
            lblError.setText("Kullanıcı adı ,password ve posizyon bilgisi giriniz");
        }


    }//GEN-LAST:event_btnUpdateActionPerformed

